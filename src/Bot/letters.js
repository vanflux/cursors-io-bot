function build() {
    for (let letter in data) {
        let item = data[letter];
        item.paths = [];
        let { raw, paths } = item;

        let width = 0;
        let height = 0;
        for (let rawItem of raw) {
            if (rawItem.length > height) height = rawItem.length;
            let path = [];
            for (let y = 0; y < rawItem.length; y++) {
                let line = rawItem[y];
                if (line.length > width) width = line.length;
                for (let x = 0; x < line.length; x++) {
                    let i = line[x];

                    if (i > 0) {
                        path.push({x, y, i});
                    }
                }
            }
            path.sort((a, b) => a.i - b.i);
            path = path.map(x => ({ x: x.x, y: x.y }));
            paths.push(path);
        }
        item.width = width;
        item.height = height;
    }
}

